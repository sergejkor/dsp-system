const localeSet = new Set(PERSONAL_QUESTIONNAIRE_LOCALES.map((item) => item.locale));

export function normalizePersonalQuestionnaireLocale(locale) {
  return localeSet.has(locale) ? locale : 'en';
}

export function isPersonalQuestionnaireRtl(locale) {
  return normalizePersonalQuestionnaireLocale(locale) === 'ar';
}

export function getPersonalQuestionnaireGermanLanguageValue(locale) {
  return PERSONAL_QUESTIONNAIRE_LOCALES.find((item) => item.locale === normalizePersonalQuestionnaireLocale(locale))?.germanValue || 'Englisch';
}

export const FORM_LANGUAGE_OPTIONS = PERSONAL_QUESTIONNAIRE_LOCALES.map((item) => ({
  value: item.germanValue,
  label: item.nativeLabel,
}));

function localizeOptionDefs(defs, locale) {
  const normalized = normalizePersonalQuestionnaireLocale(locale);
  return defs.map((item) => ({
    value: item.value,
    label: item.labels[normalized] || item.labels.en || item.value,
  }));
}

function createRegionDisplayNames(locale) {
  try {
    return new Intl.DisplayNames([locale], { type: 'region' });
  } catch {
    return null;
  }
}

export function getPersonalQuestionnaireCopy(locale) {
  const normalized = normalizePersonalQuestionnaireLocale(locale);
  return COPY[normalized] || COPY.en;
}

export function getPersonalQuestionnaireOptions(locale) {
  const normalized = normalizePersonalQuestionnaireLocale(locale);
  const localeRegions = createRegionDisplayNames(normalized);
  const germanRegions = createRegionDisplayNames('de');

  const nationalityOptions = NATIONALITY_REGION_CODES.map((code) => {
    const germanValue = germanRegions?.of(code) || code;
    const label = localeRegions?.of(code) || germanValue;
    return { value: germanValue, label };
  }).concat(
    SPECIAL_NATIONALITIES.map((item) => ({
      value: item.value,
      label: item.labels[normalized] || item.labels.en || item.value,
    }))
  );

  return {
    languages: FORM_LANGUAGE_OPTIONS,
    genders: localizeOptionDefs(OPTION_DEFS.gender, normalized),
    maritalStatuses: localizeOptionDefs(OPTION_DEFS.maritalStatus, normalized),
    yesNo: localizeOptionDefs(OPTION_DEFS.yesNo, normalized),
    churchTaxTypes: localizeOptionDefs(OPTION_DEFS.churchTaxType, normalized),
    nationalities: nationalityOptions,
  };
}

